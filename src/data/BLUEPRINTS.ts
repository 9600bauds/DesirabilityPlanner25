function validateBlueprintIds(blueprints: Record<string, Blueprint>) {
  const usedIds = new Set();
  for (const [key, blueprint] of Object.entries(blueprints)) {
    const id = blueprint.id;
    if (!id || id < 0 || id > 255) {
      throw new Error(
        `Invalid ID: "${key}" has ID ${id} which is outside the valid range (0-255)`
      );
    }
    if (usedIds.has(id)) {
      throw new Error(
        `Duplicate ID: ${id} is already used by another blueprint!`
      );
    }
    usedIds.add(id);
  }
}
